                send() {
                    let valid = true;
                    let name = document.getElementById("name").value;
                    let email = document.getElementById("email").value;
                    let subject = document.getElementById("subject").value;
                    let message = document.getElementById("message").value;
                    if (name == "") {
                        valid = false;
                        $(".error-message").text("Please enter your name");
                        $(".error-message").css("display", "block");
                    }
                    if (email == "") {
                        valid = false;
                        $(".error-message").text("Please enter your email");
                        $(".error-message").css("display", "block");
                    }
                    if (subject == "") {
                        valid = false;
                        $(".error-message").text("Please enter your subject");
                        $(".error-message").css("display", "block");
                    }
                    if (message == "") {
                        valid = false;
                        $(".error-message").text("Please enter your message");
                        $(".error-message").css("display", "block");
                    }
                    let data = {
                        type: "contactus",
                        name: name,
                        email: email,
                        subject: subject,
                        message: message
                    };
                    if (valid) {
                        $.ajax({
                            url: "dbHelper/contactus.php",
                            type: "POST",
                            data: data,
                            success: function(data) {
                                console.log(data);
                                if (data == "success") {
                                    $(".sent-message").css("display", "block");
                                    $(".error-message").css("display", "none");
                                    document.getElementById("name").value = "";
                                    document.getElementById("email").value = "";
                                    document.getElementById("subject").value = "";
                                    document.getElementById("message").value = "";
                                } else {
                                    $(".error-message").text("Something went wrong");
                                }
                            }
                        });
                    }
                }
            }
        }
        window.onload = () => {
        $('#preloader').hide();
    }
